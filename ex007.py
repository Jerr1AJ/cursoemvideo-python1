# Desenvolva um programa que leia as duas notas de um alunos, calcule e mostre a sua média

nome = input('Qual o nome do aluno?:')
nota1 = float(input('Digita a primeira nota:'))
nota2 = float(input('Digita a segunda nota:'))

media = (nota1+nota2)/2

print('A média de {} é {}'.format(nome,media))

